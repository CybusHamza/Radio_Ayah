package com.radioayah.util;

public class ExploreListData {
    String qari_Name, surah_Name;
    int likes, plays, id;

    public String getQari_Name() {
        return qari_Name;
    }

    public void setQari_Name(String qari_Name) {
        this.qari_Name = qari_Name;
    }

    public String getSurah_Name() {
        return surah_Name;
    }

    public void setSurah_Name(String surah_Name) {
        this.surah_Name = surah_Name;
    }

    public int getLikes() {
        return likes;
    }

    public void setLikes(int likes) {
        this.likes = likes;
    }

    public int getPlays() {
        return plays;
    }

    public void setPlays(int plays) {
        this.plays = plays;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
